package Basic;

public class IfElse {
    public static void main(String[] args){
        int temp = 11;

        if (temp <= 5){
            System.out.println("자켓을 입으세요");
        }
        else if (temp <= 10){
            System.out.println("자켓을 입지마세요");
        }

        else{
            System.out.println("반팔을 입으세요");
        }


    }
}
